public class HelloWorld {

    public static void main(String[] args) {

        System.out.println("Java is cool!");

        int a = 10;
        int b = 5;

        int sum = a + b;
        int sub = a - b;
        int mul = a * b;
        int div = a / b;

        System.out.println("Sum: " + sum);
        System.out.println("Sub: " + sub);
        System.out.println("Mul: " + mul);
        System.out.println("Div: " + div);

        System.out.println(a > b);
        System.out.println(a > b);
        System.out.println(a == b);

        float aFloat = 100000;
        float bFloat = 50000;

        float sumFloat = aFloat + bFloat;
        float subFloat = aFloat - bFloat;
        float mulFloat = aFloat * bFloat;
        float divFloat = aFloat / bFloat;

        System.out.println("Sum: " + sumFloat);
        System.out.println("Sub: " + subFloat);
        System.out.println("Mul: " + mulFloat);
        System.out.println("Div: " + divFloat);

        System.out.println(aFloat > bFloat);
        System.out.println(aFloat > bFloat);
        System.out.println(aFloat == bFloat);

        double aDouble = 12.4;
        double bDouble = 3.2;

        double sumDouble = aDouble + bDouble;
        double subDouble = aDouble - bDouble;
        double mulDouble = aDouble * bDouble;
        double divDouble = aDouble / bDouble;

        System.out.println("Sum: " + sumDouble);
        System.out.println("Sub: " + subDouble);
        System.out.println("Mul: " + mulDouble);
        System.out.println("Div: " + divDouble);

        System.out.println(aDouble > bDouble);
        System.out.println(aDouble > bDouble);
        System.out.println(aDouble == bDouble);
    }
}
